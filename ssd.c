//write char on font data
void ssd_writec(char page, char col, char ch, const char *font) {
	char tmp_width, tmp_height;
	const char *ptr;
	//first four bytes of the font map contains the following information
	char width=font[0], height=font[1], char_beg=font[2], char_end=font[3];
	
	if (ch < char_beg) return;
	if (ch > char_end) return;
	
	//we have a valid char
	tmp_height=0;
	height = height / 8;									//height is the number of pages
	ptr = font + 4 + (ch - char_beg) * width * height;				//starting point of the bigmap, for page 0

	//tmp_height
	do {
		//write individual pages / lines
		ssd_gotoxy(page+tmp_height, col);					//go to the coordinates
		ssd_startdat();
		for (tmp_width = 0; tmp_width < width; tmp_width++) i2c1_write(*ptr++);
		//i2c1_write(0x00);							//write 1 padding byte
		i2c1_stop();
		tmp_height+=1;							//advance to the next page
	} while (tmp_height<height);
}

//write a strong, null-terminated, font data in font
void ssd_writes(char page, char col, char *str, const char *font) {
	//ssd_gotoxy(page, col);					//goto the page
	while (*str) {
		ssd_writec(page, col, *str++, font);	//write the char
		col+=font[0];							//can be smaller, to pack more data
	}
}


//go to a x/page (0..7) and y/col(0..127)
void ssd_gotoxy(char page, char col) {
	ssd_startcmd();
	i2c1_write(0xb0 | (page & 0x07));	//set page start address
	i2c1_write(0x10 | (col >> 4));		//set the higher nibble of the column start
	i2c1_write(0x00 | (col & 0x0f));	//set the lower nibble of the column start
	i2c1_stop();
}


