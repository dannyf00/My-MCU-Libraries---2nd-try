//initilize the display
void ssd_init(void) {
	//reset the display
	i2c1_init();
	i2c1_start();								//start the transaction
	i2c1_write(I2C_CMD_WRITE | SSD_ADDR);		//send the address
	i2c1_write(0x00);							//send the control byte
	i2c1_write(0xae);							//set display on/off - ae=off, af=on
	//i2c1_write(0xd5); i2c1_write(0xf0);			//set display clock divide ratio / oscillator frequency, set divide ratio
	i2c1_write(0xa8); i2c1_write(32-1);			//set multiplex ratio (1 to 64) ... (height - 1)
	i2c1_write(0x40 | 0x00);					//set display offset. 00=no offset
	i2c1_write(0x8d); i2c1_write(0x14);			//charge pump setting, 14h=enable charge pump
	i2c1_write(0x20); i2c1_write(0x02);			//set memory addressing mode - 0b00=horizontal, 0b01=vertical, 0b10=page
	i2c1_write(0xa0 | 0x01);					//set segment re-map
	i2c1_write(0xc8); 							//set com outpt scan direction
	i2c1_write(0xda); i2c1_write(0x02);			//set com pins hardwar configuration - 128x32: 0x02, 128x64: 0x12
	//i2c1_write(0x81); i2c1_write(0x3f);			//set contrast control register
	//i2c1_write(0xd9); i2c1_write(0x22);			//set pre-charge period (0x22 or 0xf1)
	//i2c1_write(0xDB); i2c1_write(0x20);			//set vcomh deselect level - 0x00: 0.65xvcc, 0x20: 0.77xvcc
	i2c1_write(0xa4); 							//entire display on (resume) - output RAM to display
	i2c1_write(0xa6);							//set normal / inverse display mode: a6=normal; a7=inverse
	i2c1_write(0x2e); 							//deactivate scroll command
	i2c1_write(0xaf); 							//set display on/off - ae=off, af=on
	//i2c1_write(0x22); i2c1_write(0x00); i2c1_write(0x3f);	//set page address (start, end)
	//i2c1_write(0x21); i2c1_write(0x00); i2c1_write(0x7f);	//set column address (start, end)
	i2c1_stop();
}

